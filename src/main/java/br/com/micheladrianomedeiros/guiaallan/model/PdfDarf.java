package br.com.micheladrianomedeiros.guiaallan.model;

/**
 * https://www.mkyong.com/java/pdfbox-how-to-read-pdf-file-in-java/
 *
 * @author allan
 */
public class PdfDarf {

    private int idDarf;
    private String compararLinha;
    private String apuracaoDarf;
    private String inscricaoDarf;
    private String receitaDarf;
    private String vencimentoDarf;
    private String valorPrincDarf;
    private String valorMultaDarf;
    private String valorJurosDarf;
    private String valorTotalDarf;

    public String getCompararLinha() {
        return compararLinha;
    }

    public void setCompararLinha(String compararLinha) {
        this.compararLinha = compararLinha;
    }

    public int getIdDarf() {
        return idDarf;
    }

    public void setIdDarf(int idDarf) {
        this.idDarf = idDarf;
    }

    public String getApuracaoDarf() {
        return apuracaoDarf;
    }

    public void setApuracaoDarf(String apuracaoDarf) {
        this.apuracaoDarf = apuracaoDarf;
    }

    public String getInscricaoDarf() {
        return inscricaoDarf;
    }

    public void setInscricaoDarf(String inscricaoDarf) {
        this.inscricaoDarf = inscricaoDarf;
    }

    public String getReceitaDarf() {
        return receitaDarf;
    }

    public void setReceitaDarf(String receitaDarf) {
        this.receitaDarf = receitaDarf;
    }

    public String getVencimentoDarf() {
        return vencimentoDarf;
    }

    public void setVencimentoDarf(String vencimentoDarf) {
        this.vencimentoDarf = vencimentoDarf;
    }

    public String getValorPrincDarf() {
        return valorPrincDarf;
    }

    public void setValorPrincDarf(String valorPrincDarf) {
        this.valorPrincDarf = valorPrincDarf;
    }

    public String getValorMultaDarf() {
        return valorMultaDarf;
    }

    public void setValorMultaDarf(String valorMultaDarf) {
        this.valorMultaDarf = valorMultaDarf;
    }

    public String getValorJurosDarf() {
        return valorJurosDarf;
    }

    public void setValorJurosDarf(String valorJurosDarf) {
        this.valorJurosDarf = valorJurosDarf;
    }

    public String getValorTotalDarf() {
        return valorTotalDarf;
    }

    public void setValorTotalDarf(String valorTotalDarf) {
        this.valorTotalDarf = valorTotalDarf;
    }

}
